    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        // TODO add your handling code here:
        
        //Recebe os dados dos campos de nome e senha
        String username = usernameField.getText();
        String password = new String(passwordField.getPassword());
        
        //ArrayList<Cliente> clientes = clienteDAO.getAllClientes();
        boolean autenticado = false;                
        Cliente clienteAutenticado = null;
        
        //Procura e autentica o cliente  
        for (Cliente cliente : clienteDAO.getAllClientes()) {
            if (cliente.getNome().equals(username) && cliente.getPassword().equals(password)) {
                autenticado = true;
                clienteAutenticado = cliente;
                break;
            }
        }
        
        if (autenticado) {
            JOptionPane.showMessageDialog(null, "Login bem-sucedido!");
            MenuPrincipal menuPrincipal = new MenuPrincipal(clienteAutenticado, clienteDAO);
            menuPrincipal.setVisible(true);
            dispose();
            
        } else {
            JOptionPane.showMessageDialog(null, "Nome de usu√°rio ou senha incorretos!", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_loginButtonActionPerformed

